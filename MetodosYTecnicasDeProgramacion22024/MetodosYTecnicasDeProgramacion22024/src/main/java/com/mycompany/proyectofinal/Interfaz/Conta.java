    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        try {
        // Obt√©n los valores de los JLabel y convi√©rtelos a enteros
        int numero1 = Integer.parseInt(lblNumero1.getText());
        int numero2 = Integer.parseInt(lblNumero2.getText());
        int numero3 = Integer.parseInt(lblNumero3.getText());

        // Realiza la suma
        int resultado = numero1 + numero2 + numero3;

        // Verifica si el resultado es cero
        if (resultado == 0) {
            // Muestra un mensaje si todos los valores son cero
            javax.swing.JOptionPane.showMessageDialog(this, "A√∫n no existen ingresos.");
        } else {
            // Muestra el resultado en el JLabel
            lblResultado.setText(""+resultado);
        }
    } catch (NumberFormatException e) {
        // Maneja errores si el texto no es num√©rico
        lblResultado.setText("Error: valores no v√°lidos en etiquetas.");
    }
    }//GEN-LAST:event_btnCalcularActionPerformed

